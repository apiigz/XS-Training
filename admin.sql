CREATE TABLE clasesprogramadas (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    identrenamiento bigint NOT NULL,
    idprofesor bigint NOT NULL,
    fechahora timestamp without time zone NOT NULL,
    cupomaximo integer,
    CONSTRAINT fk_clase_entrenamiento FOREIGN KEY (identrenamiento) REFERENCES entrenamientos(id) ON DELETE CASCADE,
    CONSTRAINT fk_clase_profesor FOREIGN KEY (idprofesor) REFERENCES profesores(id) ON DELETE CASCADE
);
CREATE TABLE inscripciones (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    idusuario bigint,
    nombre character varying(100) NOT NULL,
    apellido character varying(100) NOT NULL,
    dni character varying(20) NOT NULL,
    numtelefono character varying(20) NOT NULL,
    correo character varying(150) NOT NULL,
    CONSTRAINT fk_inscripcion_usuario FOREIGN KEY (idusuario) REFERENCES usuario(id) ON DELETE SET NULL
);
CREATE TABLE perfiles (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    idusuario bigint NOT NULL,
    nombre character varying(100) NOT NULL,
    apellido character varying(100) NOT NULL,
    dni character varying(20) NOT NULL,
    numtelefono character varying(20) NOT NULL,
    correo character varying(150) NOT NULL,
    CONSTRAINT fk_perfil_usuario FOREIGN KEY (idusuario) REFERENCES usuario(id) ON DELETE CASCADE
);
CREATE TABLE reservas (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    idusuario bigint NOT NULL,
    idclaseprogramada bigint NOT NULL,
    fechareserva timestamp without time zone NOT NULL,
    estado estadoreserva DEFAULT 'no confirmada'::estadoreserva NOT NULL,
    CONSTRAINT fk_reserva_clase FOREIGN KEY (idclaseprogramada) REFERENCES clasesprogramadas(id) ON DELETE CASCADE,
    CONSTRAINT fk_reserva_usuario FOREIGN KEY (idusuario) REFERENCES usuario(id) ON DELETE CASCADE
);
CREATE TABLE suscripciones (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    idusuario bigint NOT NULL,
    identrenamiento bigint NOT NULL,
    fechapago date NOT NULL,
    fechavencimiento date NOT NULL,
    monto numeric(10,2) NOT NULL,
    estado estadossuscripcion DEFAULT 'pendiente'::estadossuscripcion NOT NULL,
    CONSTRAINT fk_suscripcion_entrenamiento FOREIGN KEY (identrenamiento) REFERENCES entrenamientos(id) ON DELETE CASCADE,
    CONSTRAINT fk_suscripcion_usuario FOREIGN KEY (idusuario) REFERENCES usuario(id) ON DELETE CASCADE
);
